CREATE TABLE Fruits (
    FruitID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FruitName VARCHAR2(200) NOT NULL,
    FruitType VARCHAR2(50)
);

CREATE TABLE Inventory (
    InventoryID NUMBER GENERATED BY DEFAULT AS IDENTITY  PRIMARY KEY,
    FruitID NUMBER NOT NULL,
    Price NUMBER(10, 2) NOT NULL,
    Stock NUMBER NOT NULL,
    CONSTRAINT fk_fruit FOREIGN KEY (FruitID) REFERENCES Fruits(FruitID)
);

CREATE TABLE Transactions (
    TransactionID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FruitID NUMBER NOT NULL,
    ChangeType VARCHAR2(50),
    QuantityChanged NUMBER,
    TransactionDate DATE DEFAULT SYSDATE,
    CONSTRAINT fk_fruit_trans FOREIGN KEY (FruitID) REFERENCES Fruits(FruitID)
)